CREATE TABLE USER (
    ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IS_ADMIN BOOLEAN,
    EMAIL VARCHAR(255) UNIQUE NOT NULL,
    PASSWORD VARCHAR(255) NOT NULL,
    NAME VARCHAR(255) NOT NULL,
    LAST_TOKEN VARCHAR(255),
    TOKEN_EXPIRES TIMESTAMP,
    SPRINT_POINTS INTEGER DEFAULT 0,
    COMPLETED_TASKS INTEGER DEFAULT 0
);

-- Tabela JIRA
CREATE TABLE JIRA_CARD (
    ID INTEGER PRIMARY KEY,
    TIMESTAMP BIGINT,
    ISSUE_ID VARCHAR(255),
    ISSUE_SELF VARCHAR(255),
    ISSUE_KEY VARCHAR(255),
    ISSUE_SUMMARY VARCHAR(255),
    ISSUE_CREATED TIMESTAMP,
    ISSUE_DESCRIPTION VARCHAR(1000),
    ISSUE_LABELS BLOB SUB_TYPE TEXT,
    ISSUE_PRIORITY VARCHAR(50),
    USER_SELF VARCHAR(255),
    USER_NAME VARCHAR(255),
    USER_KEY VARCHAR(255),
    USER_EMAIL VARCHAR(255),
    USER_DISPLAY_NAME VARCHAR(255),
    USER_ACTIVE BOOLEAN,
    CHANGELOG_ITEMS BLOB SUB_TYPE TEXT,
    CHANGELOG_ID INTEGER,
    COMMENT_SELF VARCHAR(255),
    COMMENT_ID VARCHAR(255),
    COMMENT_AUTHOR_SELF VARCHAR(255),
    COMMENT_AUTHOR_NAME VARCHAR(255),
    COMMENT_AUTHOR_EMAIL VARCHAR(255),
    COMMENT_BODY VARCHAR(1000),
    COMMENT_UPDATE_AUTHOR_SELF VARCHAR(255),
    COMMENT_UPDATE_AUTHOR_NAME VARCHAR(255),
    COMMENT_UPDATE_AUTHOR_EMAIL VARCHAR(255),
    COMMENT_CREATED TIMESTAMP,
    COMMENT_UPDATED TIMESTAMP,
    WEBHOOK_EVENT VARCHAR(255)
);

